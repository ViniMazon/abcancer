package com.example.tcc_abcancer.ui.model;

public class FotosModel {

        int image;
        String teste;

    public FotosModel(int image, String teste) {
        this.image = image;
        this.teste = teste;
    }

    public int getImage() {
        return image;
    }

    public String getTeste() {
        return teste;
    }
}


